import streamlit as st
import random

# リンパマッサージの場所リスト
lymphatic_points = [
    "首（首筋、鎖骨周辺）",  # 首周りのリンパを流す
    "脇の下（腋窩リンパ節）",  # 脇の下のリンパを流す
    "膝裏（膝窩リンパ節）",  # 膝裏のリンパを流す
    "足首（足首リンパ節）",  # 足首周辺のリンパを流す
    "腹部（腹部リンパ節）",  # 腹部のリンパを流す
    "腕（上腕部、肘下リンパ節）",  # 腕のリンパを流す
    "背中（背部リンパ節）",  # 背中のリンパを流す
    "顔（顔面リンパ節）",  # 顔のリンパを流す
    "太もも（大腿部リンパ節）",  # 太もも周辺のリンパを流す
    "足の裏（足裏リンパ節）"  # 足の裏のリンパを流す
]

# Streamlitのタイトルと説明
st.title("リンパマッサージをするべき場所")
st.write("今日、リンパマッサージをするべき3つの場所をランダムに提案します。リラックスして、マッサージでリフレッシュしてください！")

# ボタンをクリックしてランダムな3つの場所を表示する
if st.button("今日のリンパマッサージ場所を提案"):
    # リンパマッサージ場所リストからランダムに3つ選ぶ
    selected_points = random.sample(lymphatic_points, 3)

    # 提案されたリンパマッサージ場所を表示
    st.write("今日マッサージをするべき場所は:")
    for i, point in enumerate(selected_points, 1):
        st.write(f"{i}. {point}")
